          The Spitter Rules
        </h1>


  <p>Spitter&#39;s purpose is to serve the public conversation better than Twitter. Violence, harassment and other negative types of behavior are not acceptable in the real world, and we won't allow them on Spitter.<br />
 </p>
<h3><span>Safety</span></h3>
<p><u><br />
Violence:</u> You may not threaten violence against an individual or a group of people. We also prohibit the glorification of violence- looking at you, Donald Trump.</p>
<p><u>Terrorism/violent extremism:</u> You may not threaten or promote terrorism or violent extremism. However, you are free to protest and engage in civil disobedience through Spitter.</p>
<p><u>Child sexual exploitation:</u> We have zero tolerance for child sexual exploitation on Spitter. Our users will personally break your kneecaps if you are a pedophile, and we didn't see a thing.</p>
<p><u>Abuse/harassment:</u> You may not engage in the targeted harassment of someone, or incite other people to do so. This includes wishing or hoping that someone experiences physical harm.</p>
<p><u>Hateful conduct:</u> You may not promote violence against, threaten, or harass other people on the basis of race, ethnicity, national origin, caste, sexual orientation, gender, gender identity, religious affiliation, age, disability, or serious disease. If you are transphobic, or any other form of hateful, you will be kicked out and run over by a bus.</p>
<p><u>Perpetrators of violent attacks:</u> We will remove any accounts maintained by individual perpetrators of terrorist, violent extremist, or mass violent attacks, and may also remove Spits disseminating manifestos or other content produced by perpetrators. </p>
<p><u>Suicide or self-harm:</u> You may not promote or encourage suicide or self-harm. We can and will supply all data to law enforcement in this department.</p>
<p><u>Sensitive media is not permitted-</u> gore, porn, sexual abuse content, and any other illegal or sensitive content including NSFW artwork shall NOT be posted directly on Spitter. Certain content, like NSFW artwork, may be permitted in link form.</p>
<p><u>Illegal or certain regulated goods or services:</u> Don't get Spitter in trouble please. We will cooperate with law enforcement to a reasonable extent for most incidents. But we didn't see you distributing torrent links or discussing piracy ;)<br />
<br />
</p>
<h3><span>Privacy</span></h3>
<p><u><br />
Private information:</u> You may not publish or post other people&#39;s private information (such as home phone number and address) without their express authorization and permission. We also prohibit threatening to expose private information or incentivizing others to do so.</p>
 </p>
<h3><span>Copyright</span></h3>
<p><u><br />
Copyright:</u> We legally can't allow you to distribute copyright protected material, but discussing it and certain sites with which to torrent it isn't illegal- just speak in code ;)</p>
